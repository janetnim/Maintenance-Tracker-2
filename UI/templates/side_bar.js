function openNav() {
    document.getElementById("Sidenav").style.width = "200px";
    document.getElementById("main_section").style.marginLeft = "160px";
    document.getElementById("the_footer").style.marginLeft = "160px";
}

function closeNav() {
    document.getElementById("Sidenav").style.width = "0";
    document.getElementById("main_section").style.marginLeft = "0";
    document.getElementById("the_footer").style.marginLeft = "0";
}               